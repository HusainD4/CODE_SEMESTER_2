a = 10
b = 5
print(f'A > B = {a>b}') #lebih dari 
print(f'A < B = {a<b}') #kurang dari 
print(f'A == B = {a==b}') #kalau datanya sama maka output true, kalau berbeda maka false (sama dengan)
print(f'A != B = {a!=b}') #tidak sama dengan
print(f'A >= B = {a>=b}') #lebih dari sama dengan
print(f'A <= B = {a<=b}') #kurang dari sama dengan



