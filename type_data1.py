#contoh INT
umur = 12
print(umur)
print(type(umur))

#contoh str
nama = 'udin'
print(nama)
print(type(nama))

#contoh float
harga = 12.000
print(harga)
print(type(harga))

#contoh boolean
a = 12
b = 4
c = a == b
print(c)
print(type(c))

#cara menginput data
nama_buah = str(input("masukan nama : "))
jumlah = int(input("masukan jumlah : "))
harga = float(input("masukan harga : "))
ketersediaan = bool(input("masukan ketersediaan : "))
print(nama_buah)
print(jumlah)
print(harga)
print(ketersediaan)

